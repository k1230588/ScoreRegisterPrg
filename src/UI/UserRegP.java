    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        UserInfo ui = new UserInfo();
        List<UserInfo> list = new ArrayList();
        DBConn dbc = new DBConn();
        int sIO;

//        検索権限チェック
        if (rui.getuAdmin() == 1 && !jTextField6.getText().isEmpty()) {
            if (jTextField6.getText() != String.valueOf(rui.getuClass())) {
                jLabel11.setText("Not authorized to search this class");
                UList();
                return;
            }
        }

//        あいまい検索
        if (jCheckBox1.isSelected()) {
            sIO = 1; //あいまい検索
        } else {
            sIO = 0; //完全一致
        }

//        content取得
        if (jTextField5.getText().isEmpty() && jTextField6.getText().isEmpty() && jTextField7.getText().isEmpty()) {
            jLabel11.setText("Error: No content");
            return;
        }
        if (!jTextField5.getText().isEmpty()) {
            ui.setuID(Integer.parseInt(jTextField5.getText()));
        }
        if (!jTextField6.getText().isEmpty()) {
            ui.setuClass(Integer.parseInt(jTextField6.getText()));
        }
        if (!jTextField7.getText().isEmpty()) {
            ui.setuName(jTextField7.getText());
        } else {
            ui.setuName("null");
        }

        try {
            dbc.getDBC();
            list = dbc.SearchUL(sIO, ui, rui);
            dbc.closeDBC();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(UserRegP.class.getName()).log(Level.SEVERE, null, ex);
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getRowCount() > 0) {
            for (int j = jTable1.getRowCount(); j > 0; j--) {
                model.removeRow(0);
            }
        }
        if (!list.isEmpty()) {
            for (int i = 0; i < list.size(); i++) {
                model.addRow(new Object[]{list.get(i).getuID(), list.get(i).getuClass(), list.get(i).getuPosi(),
                    list.get(i).getuName(), list.get(i).getuPass()});
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

