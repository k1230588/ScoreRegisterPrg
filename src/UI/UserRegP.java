    public void UList() {
        List<UserInfo> list = new ArrayList();
        DBConn dbc = new DBConn();

        try {
            dbc.getDBC();
            list = dbc.userAll(rui);
            dbc.closeDBC();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(UserRegP.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (!list.isEmpty()) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            if (jTable1.getRowCount() > 0) {
                for (int j = jTable1.getRowCount(); j > 0; j--) {
                    model.removeRow(0);
                }
            }

            for (int i = 0; i < list.size(); i++) {
                model.addRow(new Object[]{list.get(i).getuID(), list.get(i).getuClass(), list.get(i).getuPosi(),
                    list.get(i).getuName(), list.get(i).getuPass()});
            }
        }
    }

